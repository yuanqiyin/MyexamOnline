package henu.bean;

public class DetailStatistics {
    private String sid;
    private String sname;
    private int score;
    private int paperid;
    private int scorses;
    private int total;
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getPaperid() {
		return paperid;
	}
	public void setPaperid(int paperid) {
		this.paperid = paperid;
	}
	public int getScorses() {
		return scorses;
	}
	public void setScorses(int scorses) {
		this.scorses = scorses;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
} 
