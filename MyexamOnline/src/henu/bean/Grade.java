package henu.bean;

public class Grade {
	private int gid;
	private int sid;
	private int paperid;
	private int score;
	private int scorses;
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public int getPaperid() {
		return paperid;
	}
	public void setPaperid(int paperid) {
		this.paperid = paperid;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getScorses() {
		return scorses;
	}
	public void setScorses(int scorses) {
		this.scorses = scorses;
	}

}
