package henu.bean;

public class ShortAnswerQuestions {
	private int s_id;
    private String s_question;
    private String s_answer;
    private int s_subjectid;
    private String s_subjectname;
    
    
    
	public String getS_subjectname() {
		return s_subjectname;
	}
	public void setS_subjectname(String s_subjectname) {
		this.s_subjectname = s_subjectname;
	}
	public int getS_id() {
		return s_id;
	}
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}
	public String getS_question() {
		return s_question;
	}
	public void setS_question(String s_question) {
		this.s_question = s_question;
	}
	public String getS_answer() {
		return s_answer;
	}
	public void setS_answer(String s_answer) {
		this.s_answer = s_answer;
	}
	public int getS_subjectid() {
		return s_subjectid;
	}
	public void setS_subjectid(int s_subjectid) {
		this.s_subjectid = s_subjectid;
	}

}
